  String t(String key, {Map<String, String>? params}) {
    final lang = locale.languageCode;
    final value = _localizedValues[lang]?[key] ?? _localizedValues['hi']?[key] ?? key;
    if (params == null || params.isEmpty) return value;
    return params.entries.fold(value, (acc, e) => acc.replaceAll('{${e.key}}', e.value));
  }

